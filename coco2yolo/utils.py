import shutil
from pathlib import Path


def make_dir(dir="new_dir/"):
    dir = Path(dir)
    if dir.exists():
        shutil.rmtree(dir)  # delete dir
    dir.mkdir(parents=True, exist_ok=True)  # make dir
    return dir


